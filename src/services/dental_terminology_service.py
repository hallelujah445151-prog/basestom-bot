import os
import httpx
from openai import OpenAI

# Загружаем переменные окружения из .env файла
try:
    from dotenv import load_dotenv
    load_dotenv()
except ImportError:
    pass


class DentalTerminologyService:
    """Сервис для коррекции стоматологической терминологии через OpenRouter AI"""

    def __init__(self):
        # Минимальная конфигурация для совместимости
        api_key = os.getenv('OPENROUTER_API_KEY')

        if not api_key:
            print("WARNING: OPENROUTER_API_KEY not found")
            self.client = None
        else:
            try:
                # Создаем кастомный httpx клиент без прокси
                http_client = httpx.Client(
                    timeout=30.0,
                    follow_redirects=True
                )

                self.client = OpenAI(
                    api_key=api_key,
                    base_url="https://openrouter.ai/api/v1",
                    http_client=http_client
                )
                print("[Dental] OpenAI client initialized successfully")
            except Exception as e:
                print(f"ERROR: Failed to initialize OpenAI client: {e}")
                print(f"Falling back to simple parser")
                self.client = None

    def normalize_work_type(self, work_type: str) -> str:
        """Нормализовать название работы с использованием стоматологической терминологии"""

        if not work_type or work_type.strip() == "":
            return work_type

        if not self.client:
            print("[Dental] OpenAI client not available, returning original")
            return work_type

        system_prompt = """
        Ты эксперт по стоматологической терминологии. Твоя задача - исправить и нормализовать название стоматологической работы на грамотный термин.

        ПРАВИЛА КОНВЕРТАЦИИ:

        1. Материалы (ОЧЕНЬ ВАЖНО - правильное написание):
           - "циркон", "циркониевый", "диоксид циркония" → "из диоксида циркония"
           - "металлокерамика", "металлокерамика", "мк" → "металлокерамическая коронка"
           - "керамика", "керамическая", "безметалловая" → "безметалловая керамическая коронка"
           - "пластмасса" → "пластмассовая коронка"
           - "цельнометаллический" → "цельнометаллическая коронка"

        2. Виды работ (правильные формы):
           - "коронка" (ед.ч.) - коронка
           - "виниры" (мн.ч.) - виниры
           - "мосты" (мн.ч.) - мосты
           - "протез" (ед.ч.) - протез
           - "шина" (ед.ч.) - шина
           - "абатмент" (ед.ч.) - абатмент
           - "вкладка" (ед.ч.) - вкладка
           - "брекет" (ед.ч.) - брекет
           - "оттиск" (ед.ч.) - оттиск

        3. Расширенные материалы и конструкции:
           - "на винте", "винт", "винтах" → "на винте"
           - "на абатменте", "абатмент", "абатменты" → "на абатменте"
           - "на имплантах", "имплант" → "на имплантах"
           - "безметалловый" → "безметалловая"
           - "полный съемный" → "полный съемный"
           - "частичный съемный" → "частичный съемный"
           - "ортодонтическая" → "ортодонтическая"
           - "иммобилизующая" → "иммобилизующая"

        4. Полные правильные названия:
           - "из диоксида циркония" (а не просто "циркон" или "циркониевый")
           - "металлокерамическая коронка" (а не просто "металлокерамика")
           - "безметалловая керамическая коронка" (а не просто "керамика")
           - "пластмассовая коронка" (а не просто "пластмасса")

        5. Грамматические правила:
           - Предлоги: "на", "из", "от" (НЕ "для" указания назначения!)
           - НЕ добавляй слова "для", "от имени", "для пациента"
           - Числительные: только если они были в оригинале
           - Род падеж: согласовывай окончания правильно

        6. Формат даты: ДД.ММ.ГГГГ

        7. Примеры правильных названий:

        ПРАВИЛЬНЫЕ ВАРИАНТЫ (выбери из этого списка или создай аналогичный):

        Коронки:
        - "цельнометаллическая коронка на винте"
        - "металлокерамическая коронка на винте"
        - "безметалловая керамическая коронка на винте"
        - "коронка из диоксида циркония на винте"
        - "пластмассовая коронка на винте"
        - "цельнометаллическая коронка на абатменте"

        Виниры:
        - "металлокерамические виниры из диоксида циркония"
        - "безметалловые виниры из диоксида циркония"
        - "керамические виниры из диоксида циркония"

        Мосты:
        - "металлокерамический мост из диоксида циркония"
        - "безметалловый мост из диоксида циркония"
        - "мост из диоксида циркония на имплантах"
        - "пластмассовый мост"

        Протезы:
        - "полный съемный протез"
        - "частичный съемный протез"
        - "бюгельный протез"

        Шинирование:
        - "временная шина"
        - "ортодонтическая шина"
        - "иммобилизующая шина"

        Вкладки:
        - "индиректная вкладка"
        - "онлеевая вкладка"
        - "вкладка из керамики"
        - "вкладка из металлокерамики"
        - "вкладка из диоксида циркония"

        Брекеты:
        - "керамические брекеты"
        - "металлические брекеты"

        Имплантация:
        - "корневая вкладка"
        - "абатмент"

        ОШИБКИ, КОТОРЫЕ НУЖНО ИСПРАВИТЬ:

        ❌ "циркон" → ✅ "из диоксида циркония"
        ❌ "циркониевый" → ✅ "из диоксида циркония"
        ❌ "цирконий" → ✅ "из диоксида циркония"
        ❌ "мк" → ✅ "металлокерамическая коронка"
        ❌ "металлокерамика" (если это вид работы) → ✅ "металлокерамическая коронка"
        ❌ "керамика" (если это вид работы) → ✅ "безметалловая керамическая коронка"
        ❌ "пластмасса" (если это вид работы) → ✅ "пластмассовая коронка"
        ❌ "на винт" → ✅ "на винте"
        ❌ "для Иванова" → ✅ (убрать "для" и оставить только "Иванов" как пациента)
        ❌ "на абатменте" → ✅ "на абатменте"
        ❌ "виниры из диоксида" → ✅ "виниры из диоксида циркония"
        ❌ "оттиск на вкладках" → ✅ "вкладки" (если контекст не требует "оттиск")

        ФОРМАТ ОТВЕТА:

        Верни ТОЛЬКО исправленное название работы в формате:
        "вид_работы [материал] [конструкция]" или "вид_работы [материал]"
        или просто "вид_работы"

        Примеры правильных ответов:

        Пример 1:
        Вход: "циркон на винте7шт"
        Выход: "цельнометаллическая коронка на винте"

        Пример 2:
        Вход: "виниры циркон"
        Выход: "керамические виниры из диоксида циркония"

        Пример 3:
        Вход: "мост циркон 4 зуба"
        Выход: "мост из диоксида циркония"

        Пример 4:
        Вход: "бюгельный протез"
        Выход: "бюгельный протез"

        Пример 5:
        Вход: "абатмент"
        Выход: "абатмент"

        Пример 6:
        Вход: "кламмеры"
        Выход: "кламмеры"

        Пример 7:
        Вход: "виниры из диоксида"
        Выход: "керамические виниры из диоксида циркония"

        Пример 8:
        Вход: "металлокерамика"
        Выход: "металлокерамическая коронка"

        Пример 9:
        Вход: "керамика"
        Выход: "безметалловая керамическая коронка"

        Пример 10:
        Вход: "пластмасса"
        Выход: "пластмассовая коронка"

        ПРИМЕЧАНИЯ:

        - Если название уже грамматически правильное, верни его как есть
        - Если название неоднозначно, выбери наиболее логичный вариант
        - Если название очень короткое, сохрани суть (например, "циркон" → "цельнометаллическая коронка на винте")
        - Всегда предпочитай более формальные термины: "из диоксида циркония" вместо "циркон"

        ТЕПЕРЬ ТЕБЯ!
        """

        user_prompt = f"""Исправь название стоматологической работы: "{work_type}"
 
        Верни ТОЛЬКО исправленное название работы в правильной форме, БЕЗ лишних слов и символов.
        НЕ добавляй объяснения, форматирование или markdown.
        Если название уже правильное, верни его без изменений.
        """

        try:
            print(f"[Dental] Processing: '{work_type[:50]}...'")
            response = self.client.chat.completions.create(
                model="meta-llama/llama-3-8b-instruct",
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt}
                ],
                temperature=0.3
            )

            corrected = response.choices[0].message.content.strip()

            # Очищаем ответ от лишних символов
            corrected = corrected.strip('"\'')  # Убираем кавычки в начале и конце
            corrected = corrected.replace('"', '').replace("'", '')  # Убираем все кавычки
            corrected = corrected.replace('\\n', ' ').replace('\\r', '')  # Убираем переносы строк
            corrected = ' '.join(corrected.split())  # Убираем лишние пробелы

            print(f"[Dental] '{work_type}' -> '{corrected}'")
            return corrected

        except Exception as e:
            print(f"[Dental] Error: {e}")
            print(f"[Dental] Falling back to original: {work_type}")
            return work_type
